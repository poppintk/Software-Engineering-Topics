package command;

public class GarageDoor {
    void up(){
        System.out.println("Door is openning");
    }
    void down(){
        System.out.println("Door is closing");
    }
    void stop(){
        System.out.println("Door is stopping");
    }
    void lightOn(){
        System.out.println("Light is on");
    }
    void lightOff(){
        System.out.println("Light is off");
    }
}
